	public static function get_lang() {
		if ( ! isset( $_SESSION ) ) {
			return 'ar';
		}
		$lang = $_SESSION['control_lang'] ?? 'ar';
		return in_array( $lang, array( 'ar', 'en' ) ) ? $lang : 'ar';
	}

	public static function t( $key ) {
		$lang = self::get_lang();
		return self::$strings[$lang][$key] ?? $key;
	}

	public static function get_all() {
		return self::$strings;
	}

