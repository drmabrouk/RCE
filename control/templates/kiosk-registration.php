        const ajaxAction = isStaff ? 'control_save_patient' : 'control_submit_kiosk_registration';

        $.post(control_ajax.ajax_url, $(this).serialize() + '&action=' + ajaxAction + '&nonce=' + control_ajax.nonce, (res) => {
            if (res.success) {
                $('#kiosk-form, #k-header, #k-step-header').hide();
                $('#kiosk-success').css('display', 'flex');

                // If it was an edit/admin action, maybe show a different message or redirect back after 3s
                if (isStaff) {
                    setTimeout(() => {
                        window.location.href = '<?php echo add_query_arg("control_view", "pediatric_records", home_url()); ?>';
                    }, 3000);
                }
            } else {
                const msg = (typeof res.data === 'string') ? res.data : (res.data.message || 'Error occurred');
                alert(msg);
                $btn.prop('disabled', false).text(kStrings[$('#k-selected-lang').val()].save);
            }
        });
    });
});
