function renderStep() {
    const $ = jQuery;
    const step = stepTree[currentStepIdx];
    const lang = $('#k-selected-lang').val();
    const s = kStrings[lang];

    // Auto-scroll to top when step changes
    $('#k-step-container').animate({ scrollTop: 0 }, 200);

    $('#k-step-content').css('opacity', 0);

    setTimeout(() => {
        let html = `<h2 style="color:var(--control-primary); font-weight:800; margin-bottom:35px; text-align:center;">${s[step.title] || step.title}</h2>`;
        html += `<div class="wiz-grid">`;

        step.fields.forEach(f => {
            const val = resumeData ? (resumeData[f] || '') : '';
            html += `<div class="wiz-field">`;
            html += `<label>${s[f] || f}</label>`;

            if (['gender', 'nationality', 'guardian_relationship', 'blood_type', 'diag_prev', 'prev_rehab_centers', 'motor_delay', 'speech_delay', 'sleep_issues', 'feeding_issues', 'eval_social', 'eval_language', 'case_classification', 'priority_level', 'suggested_pathway', 'final_decision', 'case_status', 'payment_model', 'billing_type'].includes(f)) {
                let source = f;
                if (['diag_prev', 'prev_rehab_centers', 'motor_delay', 'speech_delay', 'sleep_issues', 'feeding_issues'].includes(f)) source = 'boolean';
                if (f === 'eval_social') source = 'social';
                if (f === 'eval_language') source = 'language';
                if (f === 'guardian_relationship') source = 'relationship';
                if (f === 'case_classification') source = 'classification';
                if (f === 'priority_level') source = 'priority';
                if (f === 'suggested_pathway') source = 'pathway';
                if (f === 'final_decision') source = 'decision';
                if (f === 'case_status') source = 'status';

                html += `<select name="${f}">`;
                (optionData[source][lang] || []).forEach(opt => {
                    const selected = val == opt.v ? 'selected' : '';
                    html += `<option value="${opt.v}" ${selected}>${opt.t}</option>`;
                });
                html += `</select>`;
            } else if (['intake_reason', 'initial_behavioral_observation', 'initial_diagnosis', 'internal_notes', 'assigned_specialists'].includes(f)) {
                html += `<textarea name="${f}" rows="3">${val}</textarea>`;
            } else if (f === 'activate_account') {
                html += `<label style="display:flex; align-items:center; gap:10px; cursor:pointer; background:#f8fafc; padding:15px; border-radius:12px; border:2px solid #e2e8f0;">
                            <input type="checkbox" name="activate_account" value="1" ${val ? 'checked' : ''}>
                            <span style="font-size:0.9rem; color:var(--control-primary);">${s.account_activation}</span>
                         </label>`;
            } else {
                let type = 'text';
                if (f === 'dob') type = 'date';
                else if (f === 'email') type = 'email';
                else if (f === 'father_phone') type = 'tel';
                else if (['registration_cost', 'amount_per_cycle'].includes(f)) type = 'number';

                const required = ['name_first', 'name_second', 'name_last', 'dob', 'guardian_name', 'father_phone'].includes(f) ? 'required' : '';
                html += `<input type="${type}" name="${f}" value="${val}" ${required} step="0.01">`;
            }
            html += `</div>`;
        });

        html += `</div>`;

        $('#k-step-content').html(html).css('opacity', 1);
        updateKUI();
    }, 150);
}

function nextKStep() {
    if (validateStep()) {
        if (currentStepIdx < stepTree.length - 1) {
            currentStepIdx++;
            renderStep();
        }
    }
}

function prevKStep() {
    if (currentStepIdx > 0) {
        currentStepIdx--;
        renderStep();
    }
}

function updateKUI() {
    const $ = jQuery;
    const lang = $('#k-selected-lang').val();
    const s = kStrings[lang];
    const step = stepTree[currentStepIdx];

    $('#k-prev').toggle(currentStepIdx > 0).text(s.prev);
    $('#k-next').toggle(currentStepIdx < stepTree.length - 1).text(s.next);
    $('#k-submit').toggle(currentStepIdx === stepTree.length - 1).text(s.save);

    // Update Top Step Header
    $('#k-current-step-num').text(currentStepIdx + 1);
    $('#k-current-step-title').text(s[step.title] || step.title);
    $('#k-step-progress-text').text(`${lang === 'ar' ? 'الخطوة' : 'Step'} ${currentStepIdx + 1} / ${stepTree.length}`);

    // Generate Dots
    let dotsHtml = '';
    stepTree.forEach((step, idx) => {
        let status = '';
        if (idx === currentStepIdx) status = 'active';
        else if (idx < currentStepIdx) status = 'completed';
        dotsHtml += `<div class="k-dot ${status}" title="Step ${step.id}"></div>`;
    });
    $('#k-dots-container').html(dotsHtml);

    // Smooth entry for fields
    $('.wiz-field').each(function(i) {
        $(this).css({ 'opacity': 0, 'transform': 'translateY(10px)' });
        setTimeout(() => {
            $(this).animate({ 'opacity': 1, 'transform': 'translateY(0)' }, 300);
            $(this).css('transform', 'none'); // Fix transform stickiness
        }, i * 50);
    });
}

function validateStep() {
    let valid = true;
    jQuery('#k-step-content [required]').each(function() {
        if (!jQuery(this).val()) { jQuery(this).css('border-color', '#ef4444'); valid = false; }
        else { jQuery(this).css('border-color', ''); }
    });
    return valid;
}

function setKLang(lang) {
    const $ = jQuery;
    $('#k-selected-lang').val(lang);
    $('.lang-link').removeClass('active');
    $(`#btn-${lang}`).addClass('active');
    $('#kiosk-app-root').css('direction', lang === 'ar' ? 'rtl' : 'ltr');
    renderStep();
}

